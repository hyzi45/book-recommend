import streamlit as st

st.title("📖 도서 추천 서비스")

# 사용자 이름 입력
user_input = st.text_input("당신의 이름을 입력하세요:")

# 장르 선택
genre = st.selectbox("관심 있는 도서 장르는 무엇인가요?", 
                     ["소설", "자기계발", "과학", "IT", "에세이"])

# 도서 추천 리스트 (샘플)
books = {
    "소설": ["『1984』 - 조지 오웰", "『데미안』 - 헤르만 헤세", "『미드나잇 라이브러리』 - 맷 헤이그"],
    "자기계발": ["『몰입』 - 황농문", "『아주 작은 습관의 힘』 - 제임스 클리어"],
    "과학": ["『코스모스』 - 칼 세이건", "『시간은 흐르지 않는다』 - 카를로 로벨리"],
    "IT": ["『밑바닥부터 시작하는 딥러닝』", "『파이썬으로 데이터 주무르기』"],
    "에세이": ["『죽고 싶지만 떡볶이는 먹고 싶어』", "『무례한 사람에게 웃으며 대처하는 법』"]
}

# 사용자 맞춤 출력
if user_input:
    st.write(f"👋 {user_input} 님, {genre} 장르의 추천 도서는 다음과 같아요:")
    for book in books[genre]:
        st.markdown(f"- {book}")
